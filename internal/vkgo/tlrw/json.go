// reading functions are not stable yet, so they are not here

func JSONWriteBool(w *bytes.Buffer, v bool) {
	_, _ = w.WriteString(strconv.FormatBool(v))
}

func JSONWriteUint32(w *bytes.Buffer, v uint32) {
	var buf [128]byte
	_, _ = w.Write(strconv.AppendUint(buf[:0], uint64(v), 10))
}

func JSONWriteInt32(w *bytes.Buffer, v int32) {
	var buf [128]byte
	_, _ = w.Write(strconv.AppendInt(buf[:0], int64(v), 10))
}

func JSONWriteUint64(w *bytes.Buffer, v uint64) {
	var buf [128]byte
	_, _ = w.Write(strconv.AppendUint(buf[:0], v, 10))
}

func JSONWriteInt64(w *bytes.Buffer, v int64) {
	var buf [128]byte
	_, _ = w.Write(strconv.AppendInt(buf[:0], v, 10))
}

func JSONWriteFloat32(w *bytes.Buffer, v float32) {
	var buf [128]byte
	_, _ = w.Write(strconv.AppendFloat(buf[:0], float64(v), 'f', -1, 32))
}

func JSONWriteFloat64(w *bytes.Buffer, v float64) {
	var buf [128]byte
	_, _ = w.Write(strconv.AppendFloat(buf[:0], v, 'f', -1, 64))
}

func JSONAddCommaIfNeeded(w *bytes.Buffer) { // Never called on empty buffer, so panic is ok
	data := w.Bytes()
	lastChar := data[len(data)-1]
	if lastChar != '{' && lastChar != '[' {
		_ = w.WriteByte(',')
	}
}

